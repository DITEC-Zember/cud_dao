	/**
	 * Retrieve a single object by pk
	 * 
	 * @param pk
	 *            the primary key
	 * @throws TorqueException
	 *             Any exceptions caught during processing will be rethrown wrapped into a TorqueException.
	 * @throws NoRowsException
	 *             Primary key was not found in database.
	 * @throws TooManyRowsException
	 *             Primary key was not found in database.
	 */
	public static TTypTratovehoStroja retrieveByPK(ObjectKey pk) throws TorqueException, NoRowsException, TooManyRowsException {
		Connection db = null;
		TTypTratovehoStroja retVal = null;
		try {
			db = Torque.getConnection(DATABASE_NAME);
			retVal = retrieveByPK(pk, db);
		} finally {
			Torque.closeConnection(db);
		}
		return retVal;
	}

	/**
	 * Retrieve a single object by pk
	 * 
	 * @param pk
	 *            the primary key
	 * @param con
	 *            the connection to use
	 * @throws TorqueException
	 *             Any exceptions caught during processing will be rethrown wrapped into a TorqueException.
	 * @throws NoRowsException
	 *             Primary key was not found in database.
	 * @throws TooManyRowsException
	 *             Primary key was not found in database.
	 */
	public static TTypTratovehoStroja retrieveByPK(ObjectKey pk, Connection con) throws TorqueException, NoRowsException, TooManyRowsException {
		Criteria criteria = buildCriteria(pk);
		List v = doSelect(criteria, con);
		if (v.size() == 0) {
			throw new NoRowsException("Failed to select a row.");
		} else if (v.size() > 1) {
			throw new TooManyRowsException("Failed to select only one row.");
		} else {
			return (TTypTratovehoStroja) v.get(0);
		}
	}

	/**
	 * Retrieve a multiple objects by pk
	 * 
	 * @param pks
	 *            List of primary keys
	 * @throws TorqueException
	 *             Any exceptions caught during processing will be rethrown wrapped into a TorqueException.
	 */
	public static List retrieveByPKs(List pks) throws TorqueException {
		Connection db = null;
		List retVal = null;
		try {
			db = Torque.getConnection(DATABASE_NAME);
			retVal = retrieveByPKs(pks, db);
		} finally {
			Torque.closeConnection(db);
		}
		return retVal;
	}

	/**
	 * Retrieve a multiple objects by pk
	 * 
	 * @param pks
	 *            List of primary keys
	 * @param dbcon
	 *            the connection to use
	 * @throws TorqueException
	 *             Any exceptions caught during processing will be rethrown wrapped into a TorqueException.
	 */
	public static List retrieveByPKs(List pks, Connection dbcon) throws TorqueException {
		List objs = null;
		if (pks == null || pks.size() == 0) {
			objs = new LinkedList();
		} else {
			Criteria criteria = new Criteria();
			criteria.addIn(HIST_ID, pks);
			objs = doSelect(criteria, dbcon);
		}
		return objs;
	}

