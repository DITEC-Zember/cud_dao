	/**
	 * Retrieve a single object by pk
	 * 
	 * @param pk
	 *            the primary key
	 * @param con
	 *            the connection to use
	 * @throws TorqueException
	 *             Any exceptions caught during processing will be rethrown wrapped into a TorqueException.
	 * @throws NoRowsException
	 *             Primary key was not found in database.
	 * @throws TooManyRowsException
	 *             Primary key was not found in database.
	 */
	public static TNastupistePrm retrieveByPK(ObjectKey pk, Connection con) throws TorqueException, NoRowsException, TooManyRowsException {
		Criteria criteria = buildCriteria(pk);
		List v = doSelect(criteria, con);
		if (v.size() == 0) {
			throw new NoRowsException("Failed to select a row.");
		} else if (v.size() > 1) {
			throw new TooManyRowsException("Failed to select only one row.");
		} else {
			return (TNastupistePrm) v.get(0);
		}
	}

